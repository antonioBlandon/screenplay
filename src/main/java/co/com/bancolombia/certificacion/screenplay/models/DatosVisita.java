package co.com.bancolombia.certificacion.screenplay.models;

public class DatosVisita {
	
	private String relacionInmueble;
	private String antiguedadActividad;
	private String areaLocal;
	private String catidadPersonas;
	private String tipoDocumento;
	private String nombre;
	private String concepto;
	private String numeroDocumento;
	private String cargoEntrevistado;
	
	
	public String getRelacionInmueble() {
		return relacionInmueble;
	}
	public void setRelacionInmueble(String relacionInmueble) {
		this.relacionInmueble = relacionInmueble;
	}
	public String getAntiguedadActividad() {
		return antiguedadActividad;
	}
	public void setAntiguedadActividad(String antiguedadActividad) {
		this.antiguedadActividad = antiguedadActividad;
	}
	public String getAreaLocal() {
		return areaLocal;
	}
	public void setAreaLocal(String areaLocal) {
		this.areaLocal = areaLocal;
	}
	public String getCatidadPersonas() {
		return catidadPersonas;
	}
	public void setCatidadPersonas(String catidadPersonas) {
		this.catidadPersonas = catidadPersonas;
	}
	public String getTipoDocumento() {
		return tipoDocumento;
	}
	public void setTipoDocumento(String tipoDocumento) {
		this.tipoDocumento = tipoDocumento;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getConcepto() {
		return concepto;
	}
	public void setConcepto(String concepto) {
		this.concepto = concepto;
	}
	public String getNumeroDocumento() {
		return numeroDocumento;
	}
	public void setNumeroDocumento(String numeroDocumento) {
		this.numeroDocumento = numeroDocumento;
	}
	public String getCargoEntrevistado() {
		return cargoEntrevistado;
	}
	public void setCargoEntrevistado(String cargoEntrevistado) {
		this.cargoEntrevistado = cargoEntrevistado;
	}

}
